// ─── Calculation functions ────────────────────────────────────────────────────

/**
 * Linearly interpolate LMS parameters for a given age in months.
 * Returns [L, M, S] or null if age is outside the table range.
 */
export function interpolateLMS(table, ageMo) {
  if (ageMo < table[0][0] || ageMo > table[table.length - 1][0]) return null
  if (ageMo === table[0][0]) return [table[0][1], table[0][2], table[0][3]]
  if (ageMo === table[table.length - 1][0]) {
    const last = table[table.length - 1]
    return [last[1], last[2], last[3]]
  }
  for (let i = 0; i < table.length - 1; i++) {
    const [t0, L0, M0, S0] = table[i]
    const [t1, L1, M1, S1] = table[i + 1]
    if (ageMo >= t0 && ageMo <= t1) {
      const frac = (ageMo - t0) / (t1 - t0)
      return [
        L0 + frac * (L1 - L0),
        M0 + frac * (M1 - M0),
        S0 + frac * (S1 - S0),
      ]
    }
  }
  return null
}

/**
 * Compute Z-score from LMS parameters and measurement value.
 */
export function lmsZscore(x, L, M, S) {
  if (Math.abs(L) < 1e-6) return Math.log(x / M) / S
  return (Math.pow(x / M, L) - 1) / (L * S)
}

/**
 * Standard normal CDF using Abramowitz & Stegun approximation (error < 1.5e-7).
 */
export function normalCDF(z) {
  const a1 = 0.254829592
  const a2 = -0.284496736
  const a3 = 1.421413741
  const a4 = -1.453152027
  const a5 = 1.061405429
  const p = 0.3275911
  const sign = z < 0 ? -1 : 1
  const x = Math.abs(z) / Math.SQRT2
  const t = 1 / (1 + p * x)
  const erf = 1 - ((((a5 * t + a4) * t + a3) * t + a2) * t + a1) * t * Math.exp(-x * x)
  return 0.5 * (1 + sign * erf)
}

/**
 * Calculate percentile (0–100) for a measurement given an LMS table and age.
 * Returns null if age is outside the table range or inputs are invalid.
 */
export function calcPercentile(table, ageMo, value) {
  if (value <= 0 || ageMo < 0) return null
  const lms = interpolateLMS(table, ageMo)
  if (!lms) return null
  const [L, M, S] = lms
  const z = lmsZscore(value, L, M, S)
  const p = normalCDF(z) * 100
  return Math.max(0.1, Math.min(99.9, p))
}

/**
 * Classify percentile into a category.
 */
export function percentileCategory(p) {
  if (p === null || p === undefined) return null
  if (p < 3) return 'veryLow'
  if (p < 15) return 'low'
  if (p <= 85) return 'normal'
  if (p <= 97) return 'high'
  return 'veryHigh'
}
